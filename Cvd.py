import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
df=pd.read_csv("C:/Users/user/Downloads/archive/day_wise.csv")
print(df.head())
print(df.info())
df['Date']=pd.to_datetime(df['Date'])
print(df.isnull().sum())
df=df.sort_values('Date')
plt.figure(figsize=(10,5))
plt.plot(df['Date'], df['Confirmed'])
plt.title("COVID-! Daily Confirmed Cases")
plt.xlabel("Date")
plt.ylabel("Confirmed Cases")
plt.xticks(rotation=45)
plt.show()
plt.figure(figsize=(10,5))
plt.plot(df['Date'],df['Deaths'])
plt.title("COVID-19 Daily Deaths")
plt.xlabel("Date")
plt.ylabel("Deaths")
plt.xticks(rotation=45)
plt.show()
plt.figure(figsize=(10,5))
plt.plot(df['Date'], df['Recovered'])
plt.title("COVID-19 Recovery Trend")
plt.xlabel("Date")
plt.ylabel("Recovered Cases")
plt.xticks(rotation=45)
plt.show()
sns.lineplot(x='Date', y='Confirmed', data=df)
plt.title("Confirmed Cases Trend")
plt.show()